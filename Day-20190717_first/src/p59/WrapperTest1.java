package p59;

public class WrapperTest1 {

	public static void main(String[] args) {
		System.out.println("Byte狼 加己 蔼");
		System.out.println("Byte: {SIZE(bit) :"
				+ Byte.SIZE
				+",BYTES:"+Byte.BYTES
				+",MIN:"+Byte.MIN_VALUE
				+",MAX"+Byte.MAX_VALUE
				+ "}");
		System.out.println("boolean狼 加己 蔼");
		System.out.println("boolean: {TYPE(bit) :"
				+ Boolean.TYPE
				+",TRUE:"+Boolean.TRUE
				+",FALSE:"+Boolean.FALSE
				+",class"+Boolean.class
				+ "}");

	}

}
