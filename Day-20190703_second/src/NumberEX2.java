
public class NumberEX2 {

	public static void main(String[] args) {
		int Number1=10;
		int Number2=3;
		
		float Number7 = (float)Number1/Number2; //몫
		int Number8 = Number1%Number2; //나머지
		
		System.out.println("숫자1과 숫자2의 나눗셈 :\t"+Number7);
		System.out.println("숫자1과 숫자2의 나머지 :\t"+Number8);
	}

}
